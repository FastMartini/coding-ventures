import math
# round = rounds the number in a float.
# math.ceil = rounds up the float to the nearest int.
# math.floor = rounds down the float to the nearest int.
# abs = Will give you the absolute value of the int or float.
# pow = Will raise a base int or float to a power.
# math.sqrt = Will square root int or float.
# max = Finds the highest value within the given variables.
# min = Finds the lowest value within the given variables.

pi = 3.14
x = 1
y = 2
z = 3

print(round(pi))

print(math.ceil(pi))

print(math.floor(pi))

print(abs(pi))

print(pow(pi, 2))

print(math.sqrt(420))

print(max(x, y, z))

print(min(x, y, z))